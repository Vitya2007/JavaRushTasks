package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int b = 0;
        while (b<5)
        {

            int c = 0;
            while (c<10)
            {
                System.out.print('Q');
                c++;

            }
            System.out.println(" ");
            b++;
        }

    }
}